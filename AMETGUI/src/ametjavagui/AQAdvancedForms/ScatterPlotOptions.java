        private String symbolFormat(String str) {
            String symbol = "";
            //System.out.println(str);
            
            switch(str) {
                case "Circle":
                    symbol = "16";
                    break;
                case "Triangle":
                    symbol = "17";
                    break;
                case "Square":
                    symbol = "15";
                    break;
                case "Diamond":
                    symbol = "18";
                    break;
                case "Star":
                    symbol = "11";
                    break;
                case "Burst":
                    symbol = "8";
                    break;
                case "X":
                    symbol = "4";
                    break;
                default:
                    System.out.println("error");
                    symbol = "0";
            }
            return symbol;
        }
    
