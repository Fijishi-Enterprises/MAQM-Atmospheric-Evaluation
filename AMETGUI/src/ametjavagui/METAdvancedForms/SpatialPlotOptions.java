    private void SaveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveButtonActionPerformed
        form.hist_plot = form.checkBoxFormat(HistogramCheckBox);
        form.histplot = HistogramCheckBox.isSelected();
        form.shade_plot = form.checkBoxFormat(ShadePlotCheckBox);
        form.shadeplot = ShadePlotCheckBox.isSelected();
        form.daily = form.checkBoxFormat(DailyPlotCheckBox);
        form.dailyplot = DailyPlotCheckBox.isSelected();
        form.t_test_flag = form.checkBoxFormat(TtestCheckBox);
        form.ttest = TtestCheckBox.isSelected();
        form.check_save = form.checkBoxFormat(RedoCheckBox);
        form.checksave= RedoCheckBox.isSelected();
        form.thresh = form.numNullFormat(ThresholdTextField.getText());
        System.out.println("Spatial Surface Analysis Options Saved");
        setVisible(false);
        dispose();
    }//GEN-LAST:event_SaveButtonActionPerformed

